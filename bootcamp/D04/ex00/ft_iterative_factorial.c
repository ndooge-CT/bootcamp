int	ft_iterative_factorial(int nb)
{
	int	a;

	a = 0;
	if(nb < 0 || nb > 12)
		return 0;
	if (nb <= 1)
		return 1;
	a = 1;
	while (nb > 1)
	{
		a *= nb;
		nb--;
	}
	return a;
}

